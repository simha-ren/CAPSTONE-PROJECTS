class PriorityInMemoryBroker:
    """In-process broker using asyncio.PriorityQueue for payments and regular Queue for DLQ.
    Lower priority number means higher priority (e.g., 1 is highest, 10 is lowest)."""

    def __init__(self):
        self.queues: Dict[str, Any] = { # Use Any because 'payments' uses PriorityQueue, 'payments.dlq' uses Queue
            'payments': asyncio.PriorityQueue(),
            'payments.dlq': asyncio.Queue(),
        }
        self.stats = {'published': 0, 'consumed': 0, 'dead_lettered': 0}

    async def connect(self):
        # In-memory broker does not need an explicit connection.
        log.info('mq.broker_init', message='PriorityInMemoryBroker initialized.')
        pass

    async def disconnect(self):
        # In-memory broker does not need an explicit disconnection.
        log.info('mq.broker_shutdown', message='PriorityInMemoryBroker shut down.')
        pass

    async def publish(self, queue_name: str, body: dict, priority: int = 5):
        # Create a Message object with the given priority
        # Ensure the body passed to Message is compatible with the PaymentRequest structure
        msg = Message(body=body, priority=priority)
        if queue_name == 'payments':
            # Store as (priority, message) tuple so PriorityQueue orders by priority
            await self.queues[queue_name].put((msg.priority, msg))
        else: # For DLQ, treat as regular queue, priority does not apply here
            await self.queues[queue_name].put(msg)
        self.stats['published'] += 1
        log.info('mq.published', queue=queue_name, message_id=msg.id, body=body, priority=priority)
        return msg.id

    async def consume(self, queue_name: str) -> Optional[Message]:
        try:
            if queue_name == 'payments':
                # Get from PriorityQueue, unpack (priority, message) tuple
                priority, msg = await self.queues[queue_name].get_nowait()
                return msg
            else: # For DLQ, get a regular message
                return self.queues[queue_name].get_nowait()
        except asyncio.QueueEmpty:
            return None

    async def dead_letter(self, msg: Message, reason: str):
        msg.dead_lettered_at = time.strftime('%Y-%m-%dT%H:%M:%SZ', time.gmtime())
        msg.dead_letter_reason = reason
        # When dead-lettering, we just put the Message object directly into the DLQ
        await self.queues['payments.dlq'].put(msg)
        self.stats['dead_lettered'] += 1
        log.warning('mq.dead_lettered', message_id=msg.id, reason=reason)

    def depth(self, queue_name: str) -> int:
        return self.queues[queue_name].qsize()

