        sb_message = ServiceBusMessage(message_body)

        await self.payments_sender.send_messages(sb_message)
        self.stats['published'] += 1
        msg_id = str(uuid.uuid4()) # ASB message ID is assigned by the service; use a generated UUID for consistency
        log.info('mq.published', queue=queue_name, message_id=msg_id, body=body)
        return msg_id

