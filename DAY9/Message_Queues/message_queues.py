class RabbitMQBroker:
    """Broker implementation using aio_pika for RabbitMQ."""

    def __init__(self):
        self.connection = None
        self.channel = None
        self.payments_queue = None
        self.dlq_queue = None
        self.stats = {'published': 0, 'consumed': 0, 'dead_lettered': 0}

    async def connect(self):
        if not CLOUDAMQP_URL:
            log.error("CLOUDAMQP_URL is not set. Cannot connect to RabbitMQ.")
            raise ConnectionError("CLOUDAMQP_URL is missing.")
        log.info("Attempting to connect to RabbitMQ...")
        self.connection = await aio_pika.connect_robust(CLOUDAMQP_URL)
        self.channel = await self.connection.channel()

        # Declare DLX for dead-lettering
        dlx_exchange = await self.channel.declare_exchange(
            'dlx', aio_pika.ExchangeType.DIRECT, durable=True
        )

        # Declare payments DLQ
        self.dlq_queue = await self.channel.declare_queue(
            'payments.dlq', durable=True
        )
        await self.dlq_queue.bind(dlx_exchange, routing_key='payments.dlq')

        # Declare payments queue with DLX argument
        self.payments_queue = await self.channel.declare_queue(
            'payments', durable=True,
            arguments={
                'x-dead-letter-exchange': 'dlx',
                'x-dead-letter-routing-key': 'payments.dlq',
                'x-message-ttl': 86400000 # 24 hours TTL
            }
        )
        log.info("RabbitMQ connected and queues declared.")

    async def disconnect(self):
        if self.connection:
            await self.connection.close()
            log.info("RabbitMQ connection closed.")

    async def publish(self, queue_name: str, body: dict):
        if not self.channel:
            raise ConnectionError("RabbitMQ channel not open.")

        # Ensure body is JSON serializable
        message_body = json.dumps(body).encode()

        message = aio_pika.Message(
            message_body,
            content_type='application/json',
            delivery_mode=aio_pika.DeliveryMode.PERSISTENT
        )
        await self.channel.default_exchange.publish(
            message,
            routing_key=queue_name
        )
        self.stats['published'] += 1
        msg_id = str(uuid.uuid4()) # aio_pika doesn't easily expose message_id before publish
        log.info('mq.published', queue=queue_name, message_id=msg_id, body=body)
        return msg_id # Return a generated ID for consistency

