"""## Evaluation Metrics: MRR and NDCG

To mathematically prove how well our search engine performs, we'll use two popular metrics:

1.  **Mean Reciprocal Rank (MRR)**:
    MRR is a statistic for evaluating any process that produces a list of possible responses to a query, ordered by probability of correctness. The reciprocal rank is 1 divided by the rank of the first relevant document. If no relevant documents are found, the reciprocal rank is 0. MRR is the average of the reciprocal ranks for a set of queries.

    $\text{MRR} = \frac{1}{|Q|} \sum_{q=1}^{|Q|} \frac{1}{\text{rank}_q}$

    Where:
    *   $Q$ is the set of queries.
    *   $\text{rank}_q$ is the rank of the first relevant document for query $q$.

2.  **Normalized Discounted Cumulative Gain (NDCG)**:
    NDCG measures the usefulness, or gain, of a document based on its position in the result list. The gain is accumulated from the top of the result list to the bottom, with the gain of each result discounted at lower ranks. This metric is useful when relevance judgments are on a graded scale (e.g., highly relevant, relevant, somewhat relevant, non-relevant).

    $\text{DCG}_p = \sum_{i=1}^{p} \frac{2^{\text{rel}_i} - 1}{\log_2(i+1)}$

    $\text{IDCG}_p = \sum_{i=1}^{p} \frac{2^{\text{rel}_{i, \text{ideal}}} - 1}{\log_2(i+1)}$

    $\text{NDCG}_p = \frac{\text{DCG}_p}{\text{IDCG}_p}$

    Where:
    *   $p$ is the number of results considered.
    *   $\text{rel}_i$ is the relevance score of the result at position $i$.
    *   $\text{rel}_{i, \text{ideal}}$ is the relevance score of the result at position $i$ in the ideal ranking.

Let's implement these metrics.
"""

def calculate_mrr(ground_truth, search_function, corpus):
    """Calculates Mean Reciprocal Rank (MRR)."""
    reciprocal_ranks = []
    for query, relevant_doc_ids in ground_truth.items():
        if not relevant_doc_ids: # Skip if no relevant documents defined
            continue

        results = search_function(query, top_n=len(corpus)) # Get all results to find first relevant
        found_first_relevant = False
        for rank, (doc_id, _) in enumerate(results):
            if doc_id in relevant_doc_ids:
                reciprocal_ranks.append(1 / (rank + 1))
                found_first_relevant = True
                break
        if not found_first_relevant: # If no relevant document was found in results
            reciprocal_ranks.append(0)

    return np.mean(reciprocal_ranks) if reciprocal_ranks else 0

def calculate_ndcg(ground_truth, search_function, corpus, k=5):
    """Calculates Normalized Discounted Cumulative Gain (NDCG@k)."""
    ndcg_scores = []
    for query, relevant_doc_ids in ground_truth.items():
        if not relevant_doc_ids: # Skip if no relevant documents defined
            continue

        # Get search results
        search_results = search_function(query, top_n=k)

        # Calculate DCG
        dcg = 0.0
        for i, (doc_id, _) in enumerate(search_results):
            if doc_id in relevant_doc_ids:
                # Assuming binary relevance (1 if relevant, 0 if not)
                relevance = 1
            else:
                relevance = 0
            dcg += (2**relevance - 1) / np.log2(i + 2) # i + 2 because log2(1) is 0

        # Calculate IDCG (Ideal DCG)
        # Get relevance scores for top k ideal documents
        ideal_relevance_scores = [1] * len(relevant_doc_ids) # All relevant docs are ideal
        ideal_relevance_scores = sorted(ideal_relevance_scores, reverse=True)[:k]

        idcg = 0.0
        for i, relevance in enumerate(ideal_relevance_scores):
            idcg += (2**relevance - 1) / np.log2(i + 2)

        if idcg > 0:
            ndcg_scores.append(dcg / idcg)
        else:
            ndcg_scores.append(0) # If there are no relevant documents in ideal top k, NDCG is 0

    return np.mean(ndcg_scores) if ndcg_scores else 0

print("MRR and NDCG functions defined.")

