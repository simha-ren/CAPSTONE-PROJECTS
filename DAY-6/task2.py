"""**CRON example**"""

def cron_ex():
  cron_expression="0 8 * * *"

  print("Cron Schedule Example:")
  print(f"Runs daily at 08:00 AM --> {cron_expression}")

"""**MAIN Program**"""

def main():
  organise(SAMPLE_DATA_DIR)

  data=scrape_quotes()
  save_to_csv(data)

  schedule.every(10).seconds.do(job_scrape)
  schedule.every().day.at("08:00").do(job_report)

  print("scheduler started")
  end_time=time.time()+35

  while time.time()<end_time:
    schedule.run_pending()
    time.sleep(1)
  print("Scheduler stopped")

  scheduler = BackgroundScheduler()

# Interval job — every 15 seconds
  scheduler.add_job(job_scrape, "interval", seconds=15,
                   id="scraper")

# Cron job — weekdays at 07:30
  scheduler.add_job(job_report,
                   CronTrigger(day_of_week="mon-fri", hour=7, minute=30),
                   id="daily_report")

  scheduler.start()
  print("Scheduler running. Jobs:")
  for job in scheduler.get_jobs():
    print(f" • {job.id} — next: {job.next_run_time}")

  time.sleep(40)
  scheduler.shutdown()

  print("Scheduler stopped")
  print("Python automation projecte completed")

  argparse_ex()
  subprocess_ex()
  cron_ex()

if __name__=="__main__":
  main()