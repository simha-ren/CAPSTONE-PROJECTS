from IPython.core.application import logging
import os,logging,csv,shutil,subprocess,time,argparse
from datetime import datetime
from email.mime.text import MIMEText
from pathlib import Path
import smtplib,requests,schedule
from bs4 import BeautifulSoup
from apscheduler.schedulers.background import BackgroundScheduler
from apscheduler.triggers.cron import CronTrigger

BASE_DIR=Path.cwd()
SAMPLE_DATA_DIR=BASE_DIR/'sample_data'
QUOTES_CSV=BASE_DIR/'quotes.csv'

logging.basicConfig(level=logging.INFO,format='%(asctime)s - %(message)s',) # Added a comma for consistency
logging.info("Python automation script started")

"""**File Organizer Automation**"""

EXT_MAP = {
    ".pdf": "PDFs", ".jpg": "Images",
    ".png": "Images", ".csv": "Data",
    ".xlsx": "Data", ".mp4": "Videos",
}

def organise(folder: str):
    src = Path(folder)
    src.mkdir(exist_ok=True)
    for f in src.iterdir():
        if f.is_file():
            dest = src / EXT_MAP.get(f.suffix.lower(), "Other")
            dest.mkdir(exist_ok=True)
            shutil.move(str(f), dest / f.name)
            print(f"Moved {f.name} → {dest.name}/")

"""**WEB SCRAPING AUTOMATION**"""

def scrape_quotes() -> list:
    url = "https://quotes.toscrape.com"
    r = requests.get(url, timeout=10)
    soup = BeautifulSoup(r.text, "html.parser")
    quotes = []
    for q in soup.select(".quote")[:5]:
        quotes.append({
            "text": q.find("span", class_="text").text,
            "author": q.find("small").text,
            "scraped_at": datetime.now().isoformat(),
        })
    return quotes

"""**Save Data to CSV**"""

def save_to_csv(rows, path="quotes.csv"):
    file_exists=os.path.isfile(path)
    with open(path, "a", newline="",encoding="utf-8") as f:
        w = csv.DictWriter(f, fieldnames=["text","author","scraped_at"],)
        if not file_exists:
            w.writeheader()
        w.writerows(rows)
    print(f"Saved {len(rows)} quotes")

"""**EMAIL AUTOMATION**"""

def send_alert(subject: str, body: str,
               to: str, gmail_user: str, app_pw: str):
    try:
      msg = MIMEText(body)
      msg["Subject"] = subject
      msg["From"]    = gmail_user
      msg["To"]      = to
      with smtplib.SMTP_SSL("smtp.gmail.com", 465) as s:
        s.login(gmail_user, app_pw)
        s.send_message(msg)
      print("Alert sent successfully")
    except:
      print("Failed to send alert")

"""**Scheduled automation jobs**"""

def job_scrape():
    data = scrape_quotes()
    save_to_csv(data)
    print(f"[{datetime.now():%H:%M:%S}] Scraped {len(data)} rows")

def job_report():
    print(f"[{datetime.now():%H:%M:%S}] Daily report sent")

